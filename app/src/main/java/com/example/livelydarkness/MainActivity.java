package com.example.livelydarkness;

import androidx.appcompat.app.AppCompatActivity;

import android.os.Bundle;
import android.view.View;
import android.widget.Button;
import android.widget.TextView;

import java.io.File;
import java.io.FileReader;
import java.io.FileWriter;

public class MainActivity extends AppCompatActivity {
    private File dummyFile;

    @Override
    protected void onCreate(Bundle savedInstanceState) {
        super.onCreate(savedInstanceState);
        setContentView(R.layout.activity_main);

        // creating empty fodder file
        // need to replace with the file generated by geofence
        File dir = this.getFilesDir();
        dummyFile = new File(dir, "fodder");

        try {
            FileWriter fw = new FileWriter(dummyFile);
            fw.write("ENTER 0100 EXIT 0200 ENTER 0300 EXIT 0400 ENTER 1000");
            fw.close();
        } catch (Exception e) {
            e.printStackTrace();
        }
    }

    /**
     * Function of reading the given file.
     * @return String that is "ENTER \d*...EXIT \d*..."
     */
    private String fileToStr(File file) {

        StringBuilder builder = new StringBuilder();

        try {
            FileReader fr = new FileReader(file);

            for (int c = fr.read() ; c != -1 ; c = fr.read()) {
                builder.append((char) c);
            }
            fr.close();
        } catch (Exception e) {
            e.printStackTrace();
        }

        return builder.toString();
    }

    public void buttonClicked(View view) {
        String rawData = fileToStr(dummyFile);
        CalculateTime obj = new CalculateTime(rawData);

        long[] result = obj.calculateTimeDiff();
        ((TextView) findViewById(R.id.enterTime)).setText(String.format("%d", result[0]));
        ((TextView) findViewById(R.id.exitTime)).setText(String.format("%d", result[1]));
    }

}
